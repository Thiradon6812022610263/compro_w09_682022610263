#include <stdio.h>

int main() {
    int sum, p, x, y;
    sum = 0;
    for (x = 1, y = 1; x * y <= 15; x++, y += 2) {
        p = x * y;
        sum = sum + p;
        printf("%d * %d = %d\n", x, y, p);
    }
    printf("summation of x * y = %d\n", sum);
    return 0;
}
/*
 รอบที่	x	y	p = x*y	sum ก่อน	sum หลัง	เงื่อนไข x*y <= 15
1	   1  1	    1	    0	         1	          true
2	   2  3	    6	    1	         7	          true
3	   3  5	    15	    7	        22	          true
4	   4  7	   28	   22	        50	          false*/
/*สรุปและอภิปรายผลการทดลอง

จากการทดลอง โปรแกรมจะวนลูปคำนวณค่า x * y โดยที่ x เพิ่มทีละ 1 และ y เพิ่มทีละ 2 ในแต่ละรอบ เริ่มจากค่าเริ่มต้น x = 1, y = 1 และจะหยุดเมื่อ x * y > 15 ในแต่ละรอบจะนำผลคูณที่ได้ไปบวกสะสมในตัวแปร sum พร้อมแสดงค่าของ x, y, และ p (ผลคูณ) ออกทางหน้าจอ

อภิปรายผล

โปรแกรมนี้แสดงให้เห็นถึงการใช้ for loop ที่มีตัวแปรควบคุมมากกว่า 1 ตัว และการเปลี่ยนแปลงค่าของตัวแปรในแต่ละรอบ
การเพิ่มค่า x และ y ในแต่ละรอบ ส่งผลให้ค่าของ x * y เปลี่ยนแปลงไปเรื่อย ๆ และเมื่อเกิน 15 โปรแกรมจะหยุดทำงาน
ผลลัพธ์ที่ได้คือ ผลคูณแต่ละรอบ (1, 6, 15) และผลรวมสุดท้าย (sum = 22)
การแสดงค่าตัวแปรในแต่ละรอบช่วยให้เข้าใจการทำงานของลูปและการเปลี่ยนแปลงค่าตัวแปรได้ชัดเจน*/